def _expand_sweep(
    sigma_grid: Tuple[float, ...], seeds: Tuple[int, ...]
) -> List[Tuple[float, int]]:
    """Build the (sigma, seed) list. σ=0 is deterministic so only seed[0] is used."""
    out: List[Tuple[float, int]] = []
    for sigma in sigma_grid:
        if sigma == 0.0:
            out.append((0.0, int(seeds[0])))
        else:
            for s in seeds:
                out.append((float(sigma), int(s)))
    return out


def _run_dir_for(base: Path, sigma: float, seed: int) -> Path:
    return base / f"sigma_{sigma:.2f}_seed_{seed}"


def _collect_robustness(
    base: Path, sweep: List[Tuple[float, int]]
) -> pd.DataFrame:
    """Concatenate every (σ, seed) run's comparison_wide.csv into one frame."""
    frames: List[pd.DataFrame] = []
    for sigma, seed in sweep:
        wide_path = _run_dir_for(base, sigma, seed) / "compare" / "comparison_wide.csv"
        if not wide_path.exists():
            print(f"[WARN] missing wide CSV: {wide_path}")
            continue
        df = pd.read_csv(wide_path)
        df["noise_sigma"] = float(sigma)
        df["noise_seed"] = int(seed)
        frames.append(df)
    if not frames:
        return pd.DataFrame()
    return pd.concat(frames, ignore_index=True)


def _aggregate_per_country(full: pd.DataFrame) -> pd.DataFrame:
    """For each (country, sigma): average MIS across seeds, per method.

    Output columns: country, noise_sigma, {method}_mis_mean, {method}_mis_std,
    {method}_mis_n_seeds  for method ∈ {van, raw, safe, oracle_c, oracle_d, hybrid}.
    """
    if full.empty:
        return pd.DataFrame()
    methods = ("van", "raw", "safe", "oracle_c", "oracle_d", "hybrid")
    rows: List[Dict[str, object]] = []
    for (country, sigma), grp in full.groupby(["country", "noise_sigma"]):
        rec: Dict[str, object] = {
            "country": country, "noise_sigma": float(sigma),
            "n_seeds": int(grp["noise_seed"].nunique()),
        }
        for m in methods:
            col = f"{m}_mis"
            if col in grp.columns:
                vals = grp[col].dropna().to_numpy(dtype=float)
                rec[f"{m}_mis_mean"] = float(np.mean(vals)) if vals.size else float("nan")
                rec[f"{m}_mis_std"] = float(np.std(vals, ddof=1)) if vals.size > 1 else 0.0
                rec[f"{m}_mis_n"] = int(vals.size)
        rows.append(rec)
    out = pd.DataFrame(rows).sort_values(["country", "noise_sigma"]).reset_index(drop=True)
    return out


def _aggregate_overall(full: pd.DataFrame) -> pd.DataFrame:
    """For each (sigma, method): mean MIS over all (country, seed) cells.

    This is the headline "robustness curve" figure in the paper. Output:
      noise_sigma, n_observations, van_mis_mean, van_mis_std, ...
    """
    if full.empty:
        return pd.DataFrame()
    methods = ("van", "raw", "safe", "oracle_c", "oracle_d", "hybrid")
    rows: List[Dict[str, object]] = []
    for sigma, grp in full.groupby("noise_sigma"):
        rec: Dict[str, object] = {
            "noise_sigma": float(sigma),
            "n_countries": int(grp["country"].nunique()),
            "n_seeds": int(grp["noise_seed"].nunique()),
            "n_observations": int(len(grp)),
        }
        for m in methods:
            col = f"{m}_mis"
            if col in grp.columns:
                vals = grp[col].dropna().to_numpy(dtype=float)
                rec[f"{m}_mis_mean"] = float(np.mean(vals)) if vals.size else float("nan")
                rec[f"{m}_mis_std"] = float(np.std(vals, ddof=1)) if vals.size > 1 else 0.0
                # Win rate vs vanilla at this σ (per country×seed cell)
                if m != "van":
                    van_vals = grp[f"van_mis"].to_numpy(dtype=float) \
                        if "van_mis" in grp.columns else None
                    if van_vals is not None and vals.size == van_vals.size:
                        wins = int((vals < van_vals).sum())
                        rec[f"{m}_wins_vs_van"] = wins
                        rec[f"{m}_total_cells"] = int(vals.size)
        rows.append(rec)
    out = pd.DataFrame(rows).sort_values("noise_sigma").reset_index(drop=True)
    return out


def _format_summary_text(agg: pd.DataFrame) -> str:
    """Paper-ready ASCII table — one row per σ, columns are method MIS mean±std."""
    if agg.empty:
        return "(no robustness data)"
    methods = (
        ("van", "VAN"),
        ("raw", "RAW"),
        ("safe", "SAFE"),
        ("oracle_c", "ORC_C"),
        ("oracle_d", "ORC_D"),
        ("hybrid", "HYBRID"),
    )
    lines: List[str] = []
    lines.append("Robustness summary — MIS (mean ± std across countries × seeds)")
    lines.append("-" * 100)
    header = f"{'σ':>6}  {'n_obs':>6}  " + "  ".join(
        f"{lbl:>16}" for _, lbl in methods
    )
    lines.append(header)
    lines.append("-" * 100)
    for _, row in agg.iterrows():
        cells: List[str] = []
        for key, _lbl in methods:
            mean = row.get(f"{key}_mis_mean", float("nan"))
            std = row.get(f"{key}_mis_std", float("nan"))
            cells.append(f"{mean:>7.4f}±{std:>5.4f}")
        lines.append(
            f"{float(row['noise_sigma']):>6.2f}  {int(row['n_observations']):>6d}  "
            + "  ".join(cells)
        )
    return "\n".join(lines)


def _build_noiseless_paper_table(baseline_wide: pd.DataFrame) -> pd.DataFrame:
    """Build paper-ready noiseless table from baseline comparison_wide.csv.

    Output schema mirrors the manuscript table:
      country, van, raw, oracle_d, hybrid, delta_pct, hybrid_source
    where delta_pct = 100 * (van - hybrid) / van.
    """
    if baseline_wide.empty:
        return pd.DataFrame()
    req = {"country", "van_mis", "raw_mis", "oracle_d_mis", "hybrid_mis"}
    if not req.issubset(set(baseline_wide.columns)):
        return pd.DataFrame()
    df = baseline_wide.copy()
    df["delta_pct"] = 100.0 * (df["van_mis"] - df["hybrid_mis"]) / df["van_mis"]
    out = pd.DataFrame({
        "country": df["country"],
        "van": df["van_mis"],
        "raw": df["raw_mis"],
        "oracle_d": df["oracle_d_mis"],
        "hybrid": df["hybrid_mis"],
        "delta_pct": df["delta_pct"],
        "hybrid_source": df.get("hybrid_source", ""),
    })
    # Keep paper country order as emitted by pipeline; append mean row.
    mean_row = {
        "country": "MEAN",
        "van": float(out["van"].mean()),
        "raw": float(out["raw"].mean()),
        "oracle_d": float(out["oracle_d"].mean()),
        "hybrid": float(out["hybrid"].mean()),
        "delta_pct": float(out["delta_pct"].mean()),
        "hybrid_source": "",
    }
    out = pd.concat([out, pd.DataFrame([mean_row])], ignore_index=True)
    return out


def _to_latex_noiseless(tbl: pd.DataFrame) -> str:
    """Render the noiseless main table as a compact LaTeX tabular block."""
    if tbl.empty:
        return "% empty noiseless table\n"
    lines: List[str] = []
    lines.append("\\begin{tabular}{lccccc}")
    lines.append("\\toprule")
    lines.append("Country & VAN & RAW & ORACLE\\_D & HYBRID & $\\Delta\\%$ \\\\")
    lines.append("\\midrule")
    for _, r in tbl.iterrows():
        c = str(r["country"])
        if c == "MEAN":
            lines.append("\\midrule")
            lines.append(
                f"\\textbf{{MEAN}} & \\textbf{{{r['van']:.4f}}} & "
                f"\\textbf{{{r['raw']:.4f}}} & \\textbf{{{r['oracle_d']:.4f}}} & "
                f"\\textbf{{{r['hybrid']:.4f}}} & \\textbf{{{r['delta_pct']:+.2f}\\%}} \\\\"
            )
        else:
            lines.append(
                f"{c} & {r['van']:.4f} & {r['raw']:.4f} & {r['oracle_d']:.4f} & "
                f"\\textbf{{{r['hybrid']:.4f}}} & {r['delta_pct']:+.2f}\\% \\\\"
            )
    lines.append("\\bottomrule")
    lines.append("\\end{tabular}")
    return "\n".join(lines) + "\n"


def _to_latex_robustness(agg: pd.DataFrame) -> str:
    """Render robustness aggregate as LaTeX table (mean±std MIS)."""
    if agg.empty:
        return "% empty robustness table\n"
    methods = ("van", "raw", "safe", "oracle_c", "oracle_d", "hybrid")
    label = {
        "van": "VAN",
        "raw": "RAW",
        "safe": "SAFE",
        "oracle_c": "ORACLE$_C$",
        "oracle_d": "ORACLE$_D$",
        "hybrid": "HYBRID",
    }
    lines: List[str] = []
    lines.append("\\begin{tabular}{c" + "c" * len(methods) + "}")
    lines.append("\\toprule")
    lines.append("$\\sigma$ & " + " & ".join(label[m] for m in methods) + " \\\\")
    lines.append("\\midrule")
    for _, r in agg.iterrows():
        cells: List[str] = []
        for m in methods:
            mu = float(r.get(f"{m}_mis_mean", float("nan")))
            sd = float(r.get(f"{m}_mis_std", float("nan")))
            cells.append(f"{mu:.4f}$\\pm${sd:.4f}")
        lines.append(f"{float(r['noise_sigma']):.2f} & " + " & ".join(cells) + " \\\\")
    lines.append("\\bottomrule")
    lines.append("\\end{tabular}")
    return "\n".join(lines) + "\n"


