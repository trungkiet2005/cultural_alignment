#!/usr/bin/env python3
"""
Paper sweep — Open-Ended HYBRID SWA-DPBR — Logit-Noise Robustness Study
=======================================================================
Kaggle OFFLINE version — no Internet, no git clone, no pip install.

Goal
----
Quantify how each method (vanilla, RAW SWA-DPBR, SAFE blend, ORACLE_C,
ORACLE_D, HYBRID) degrades under additive Gaussian noise on the actor's
A/B-token logit gap. This complements the noiseless table
(``tab:safe_swa_results``) with a robustness curve suitable for the
paper's "Sensitivity / Stability" section.

Setup
-----
For each scenario s and persona p, the noiseless judge delta δ̂_{s,p} is
already cached in ``combined/{country}.jsonl`` from a prior noiseless
run. The robustness pipeline only re-runs the **downstream** stages
(DPBR + safe-blend + oracle + hybrid) on perturbed inputs:

    δ_{s,p}(σ, seed) = δ̂_{s,p}  +  η_{s,p}     η_{s,p} ~ iid N(0, σ²)

where η is generated by a PRNG seeded reproducibly per
(noise_seed, country) so:

  - Same (σ, seed) → identical noisy deltas → bit-reproducible MIS.
  - Different seeds → independent draws → Monte-Carlo error bars.
  - Different countries under the same seed are decorrelated.

We sweep σ across multiple magnitudes relative to the empirical δ scale
(|δ| ≈ 5–10 from the actor self-test) and average MIS across multiple
seeds at every σ > 0:

    SIGMA_GRID = [0.0, 0.25, 0.5, 1.0, 2.0]
    SEEDS      = [42, 123, 456]

σ = 0 is the noiseless baseline (single deterministic run; multiple
seeds would give identical results). σ > 0 produces 3 seeded runs each
so the per-method curve has mean ± std error bars.

Compute budget
--------------
* 1 baseline run (σ=0): full pipeline including model load + actor
  generation. ≈3–5h on Kaggle RTX 6000 96GB for 20 countries × 500
  scenarios × 5 personas. Populates the shared combined/ cache.
* 12 noise runs (σ ∈ {0.25, 0.5, 1.0, 2.0} × seeds {42, 123, 456}):
  ``skip_model_load=True``, ``combined_dir_override`` points to the
  baseline's combined/. Each run reuses cached deltas, only re-runs
  DPBR + AMCE + safe-blend + hybrid. ≈10–20 min per run = ~3–4 h total.

Total ≈ 7–9 h, comfortably under the 12h Kaggle limit.

Outputs
-------
Each (σ, seed) writes to a dedicated subdirectory under
``results/openended_robust/``::

    results/openended_robust/
    ├── combined/                          (shared cache, one set of clean deltas)
    │   ├── USA.jsonl, USA.csv
    │   └── ...
    ├── sigma_0.00_seed_42/                (baseline)
    │   ├── safe/{country}/...
    │   └── compare/comparison_wide.csv
    ├── sigma_0.25_seed_42/
    ├── sigma_0.25_seed_123/
    ├── ...
    └── compare_robust/                    (aggregated)
        ├── robustness_full.csv            (long: every (sigma, seed, country, method))
        ├── robustness_per_country.csv     (mean±std MIS per country per (method, sigma))
        ├── robustness_aggregate.csv       (mean±std MIS per (method, sigma) across countries+seeds)
        └── robustness_summary.txt         (paper-ready ASCII table)

The paper's robustness figure / table can be drawn directly from
``robustness_aggregate.csv``.

Configuration
-------------
Edit the RUN CONFIG block near the top of this file. Set
``RUN_BATCH="b1"`` to start with 10 countries if running for the first
time on a tight session.

Usage
-----
    !python /kaggle/input/cultural-alignment/exp_paper/exp_paper_openended_robust.py
"""

