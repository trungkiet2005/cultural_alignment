def main() -> None:
    if RUN_BATCH not in _BATCHES:
        raise ValueError(f"RUN_BATCH must be one of {sorted(_BATCHES)}, got {RUN_BATCH!r}")

    countries = list(_BATCHES[RUN_BATCH])
    base_root = Path(
        f"{WORK_DIR}/results/openended_robust" if _on_kaggle() else
        str(Path(__file__).parent.parent / "results" / "openended_robust")
    )
    base_root.mkdir(parents=True, exist_ok=True)
    shared_combined = base_root / "combined"
    shared_combined.mkdir(parents=True, exist_ok=True)

    sweep = _expand_sweep(SIGMA_GRID, SEEDS)
    print(f"\n[ROBUST] sweep size = {len(sweep)}  countries={countries}")
    for sigma, seed in sweep:
        print(f"    σ={sigma:.2f}  seed={seed}  → {_run_dir_for(base_root, sigma, seed).name}")

    print(f"\n[ROBUST] shared combined cache: {shared_combined}")
    print(f"[ROBUST] base_root: {base_root}")

    # ── Step 1: noiseless baseline ─────────────────────────────────────────
    # Runs full pipeline (model load + actor generation + DPBR + AMCE +
    # safe-blend + hybrid). Populates the shared combined/ cache. All
    # subsequent (σ>0, seed) runs re-use this cache via combined_dir_override.
    sigma0, seed0 = sweep[0]
    assert sigma0 == 0.0, "first sweep entry must be the σ=0 baseline"
    out0 = _run_dir_for(base_root, sigma0, seed0)
    out0.mkdir(parents=True, exist_ok=True)
    print(f"\n{'='*70}\n[ROBUST 1/{len(sweep)}] BASELINE σ=0 seed={seed0}\n{'='*70}")
    cfg0 = _make_cfg(
        countries=countries, out_dir=out0,
        combined_dir_override=shared_combined,
        sigma=sigma0, seed=seed0,
        skip_model_load=False,  # baseline does the heavy generation
    )
    run_unified_safe(cfg0)
    gc.collect()

    # ── Step 2: noise sweep ────────────────────────────────────────────────
    # skip_model_load=True for every σ>0 run. Each run re-applies a fresh
    # noise realization (controlled by SeedSequence in unified_actor_judge_safe)
    # to the cached deltas and re-runs DPBR + AMCE + safe-blend + hybrid only.
    for ix, (sigma, seed) in enumerate(sweep[1:], start=2):
        out_dir = _run_dir_for(base_root, sigma, seed)
        out_dir.mkdir(parents=True, exist_ok=True)
        print(f"\n{'='*70}\n[ROBUST {ix}/{len(sweep)}] σ={sigma:.2f} seed={seed}\n{'='*70}")
        cfg = _make_cfg(
            countries=countries, out_dir=out_dir,
            combined_dir_override=shared_combined,
            sigma=sigma, seed=seed,
            skip_model_load=True,
        )
        run_unified_safe(cfg)
        gc.collect()

    # ── Step 3: aggregate ──────────────────────────────────────────────────
    print(f"\n{'='*70}\n[ROBUST] AGGREGATING — {len(sweep)} runs\n{'='*70}")
    cmp_dir = base_root / "compare_robust"
    cmp_dir.mkdir(parents=True, exist_ok=True)

    full = _collect_robustness(base_root, sweep)
    if full.empty:
        print("[ROBUST] no per-run wide tables found — nothing to aggregate.")
        return
    full_path = cmp_dir / "robustness_full.csv"
    full.to_csv(full_path, index=False)
    print(f"[ROBUST] wrote long-format full table: {full_path}")

    per_country = _aggregate_per_country(full)
    pc_path = cmp_dir / "robustness_per_country.csv"
    per_country.to_csv(pc_path, index=False)
    print(f"[ROBUST] wrote per-country aggregate: {pc_path}")

    overall = _aggregate_overall(full)
    ov_path = cmp_dir / "robustness_aggregate.csv"
    overall.to_csv(ov_path, index=False)
    print(f"[ROBUST] wrote overall aggregate:    {ov_path}")

    summary_text = _format_summary_text(overall)
    summary_path = cmp_dir / "robustness_summary.txt"
    summary_path.write_text(summary_text, encoding="utf-8")
    print(f"[ROBUST] wrote summary table:        {summary_path}")
    print()
    print(summary_text)

    # ── Step 4: paper bundle (one-click NeurIPS artifacts) ────────────────
    # Export (a) main noiseless table and (b) robustness table in CSV + TeX
    # so the user can run this single script and paste outputs directly.
    baseline_wide_path = _run_dir_for(base_root, sigma0, seed0) / "compare" / "comparison_wide.csv"
    if baseline_wide_path.exists():
        baseline_wide = pd.read_csv(baseline_wide_path)
        noiseless_tbl = _build_noiseless_paper_table(baseline_wide)
        if not noiseless_tbl.empty:
            noiseless_csv = cmp_dir / "paper_noiseless_table.csv"
            noiseless_tbl.to_csv(noiseless_csv, index=False)
            noiseless_tex = cmp_dir / "paper_noiseless_table.tex"
            noiseless_tex.write_text(_to_latex_noiseless(noiseless_tbl), encoding="utf-8")
            print(f"[ROBUST] wrote paper noiseless CSV:  {noiseless_csv}")
            print(f"[ROBUST] wrote paper noiseless TeX:  {noiseless_tex}")
    robust_tex = cmp_dir / "paper_robustness_table.tex"
    robust_tex.write_text(_to_latex_robustness(overall), encoding="utf-8")
    print(f"[ROBUST] wrote paper robust TeX:     {robust_tex}")

    # Persist the sweep + hyperparameters + grid to a sidecar JSON for
    # reproducibility traceability in the paper appendix.
    meta = {
        "sigma_grid": list(SIGMA_GRID),
        "seeds": list(SEEDS),
        "n_scenarios": RUN_N_SCENARIOS,
        "run_batch": RUN_BATCH,
        "countries": countries,
        "model": ACTOR_MODEL_PATH,
        "alpha_max": ALPHA_MAX,
        "dpbr_r_min": DPBR_R_MIN,
        "min_vanilla_conf": MIN_VANILLA_CONF,
        "magnitude_ratio_max": MAGNITUDE_RATIO_MAX,
        "blend_floor": BLEND_FLOOR,
        "persona_std_max": PERSONA_STD_MAX,
        "country_min_alpha": COUNTRY_MIN_ALPHA,
        "exp24_var_scale": os.environ.get("EXP24_VAR_SCALE", ""),
        "noise_model": "additive Gaussian on judge δ (logit gap)",
        "noise_seeding": (
            "np.random.SeedSequence([noise_seed, hash(country) & 0xFFFFFFFF]); "
            "iid draws applied to vanilla and 4 persona deltas per scenario"
        ),
    }
    (cmp_dir / "robustness_meta.json").write_text(
        json.dumps(meta, ensure_ascii=False, indent=2), encoding="utf-8",
    )
    print(f"[ROBUST] wrote sidecar meta:         {cmp_dir/'robustness_meta.json'}")


if __name__ == "__main__":
    main()
